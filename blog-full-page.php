      <p>To shed light on how these trends are playing out within SMBs, LastPass recently polled more than 600 business and IT security leaders from companies with fewer than 3,000 employees. <br> <span class="underline">The survey revealed a concerning pattern</span>: while SMB leaders are becoming more proactive in their approach to cybersecurity – increasing awareness of and investment in security measures, for example – respondents identified that human factors are still creating serious security gaps that can leave these organizations vulnerable from cybercriminals.</p>

      <p>How can SMB leaders fill these gaps? Read on for analysis and advice from the LastPass threat intelligence team.</p>
      <h3>
      Revealing the Accountability Disconnect
      </h3>
      <p>The most salient phenomenon the survey identified is the accountability disconnect between executive actions and employee behaviors.</p>
      <p>Executives are increasing their focus on and investment in cybersecurity, with 90% of IT leaders and 80% of non-IT leaders reporting an increased focus on cybersecurity measures over the past year, and 82% of businesses boosting their cybersecurity budgets.</p>
      <p>In turn, 92% of executives and 93% of IT leaders reported believing employees understand security expectations. And the majority of executives and IT leaders reported feeling confident about their cybersecurity measures, with only 30% of leaders believing their company faces a high risk of cybersecurity issues.</p>
      <h5>While SMB leaders are becoming more proactive in their approach to cybersecurity, human factors can still create serious security gaps that leave these organizations vulnerable.</h5>
